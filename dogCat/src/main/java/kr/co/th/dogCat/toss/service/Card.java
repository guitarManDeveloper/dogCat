package kr.co.th.dogCat.toss.service;

public class Card {
    private String ownerType;
    private String number;
    private String amount;
    private String acquireStatus;
    private String isInterestFree;
    private String cardType;
    private String approveNo;
    private String installmentPlanMonths;
    private String interestPayer;
    private String issuerCode;
    private String acquirerCode;
    private String useCardPoint;
    
    
    
    
    
    
    
    
    
	public String getOwnerType() {
		return ownerType;
	}
	public void setOwnerType(String ownerType) {
		this.ownerType = ownerType;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getAcquireStatus() {
		return acquireStatus;
	}
	public void setAcquireStatus(String acquireStatus) {
		this.acquireStatus = acquireStatus;
	}
	public String getIsInterestFree() {
		return isInterestFree;
	}
	public void setIsInterestFree(String isInterestFree) {
		this.isInterestFree = isInterestFree;
	}
	public String getCardType() {
		return cardType;
	}
	public void setCardType(String cardType) {
		this.cardType = cardType;
	}
	public String getApproveNo() {
		return approveNo;
	}
	public void setApproveNo(String approveNo) {
		this.approveNo = approveNo;
	}
	public String getInstallmentPlanMonths() {
		return installmentPlanMonths;
	}
	public void setInstallmentPlanMonths(String installmentPlanMonths) {
		this.installmentPlanMonths = installmentPlanMonths;
	}
	public String getInterestPayer() {
		return interestPayer;
	}
	public void setInterestPayer(String interestPayer) {
		this.interestPayer = interestPayer;
	}
	public String getIssuerCode() {
		return issuerCode;
	}
	public void setIssuerCode(String issuerCode) {
		this.issuerCode = issuerCode;
	}
	public String getAcquirerCode() {
		return acquirerCode;
	}
	public void setAcquirerCode(String acquirerCode) {
		this.acquirerCode = acquirerCode;
	}
	public String getUseCardPoint() {
		return useCardPoint;
	}
	public void setUseCardPoint(String useCardPoint) {
		this.useCardPoint = useCardPoint;
	}


}
